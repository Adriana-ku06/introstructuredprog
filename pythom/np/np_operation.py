import numpy as np
#operaciones con los valores de los arreglos
arr_1 = np.arange(1,6)
arr_2 = np.array([-3,7,3,13,0])

# Suma
suma=np.add(arr_1, arr_2)
print(suma)
# Resta
resta=np.subtract(arr_2, arr_1)
print(resta)
# Raiz cuadrada
raiz=np.sqrt(arr_1)
print(raiz)
# Potencia
potencia=np.power(arr_1, 2)
print(potencia)
